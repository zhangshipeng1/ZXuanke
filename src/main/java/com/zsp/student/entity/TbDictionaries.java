package com.zsp.student.entity;


public class TbDictionaries {

  private long dId;
  private String dTypecode;
  private String dTypename;
  private String dItermname;
  private long dSort;


  public long getDId() {
    return dId;
  }

  public void setDId(long dId) {
    this.dId = dId;
  }


  public String getDTypecode() {
    return dTypecode;
  }

  public void setDTypecode(String dTypecode) {
    this.dTypecode = dTypecode;
  }


  public String getDTypename() {
    return dTypename;
  }

  public void setDTypename(String dTypename) {
    this.dTypename = dTypename;
  }


  public String getDItermname() {
    return dItermname;
  }

  public void setDItermname(String dItermname) {
    this.dItermname = dItermname;
  }


  public long getDSort() {
    return dSort;
  }

  public void setDSort(long dSort) {
    this.dSort = dSort;
  }

}
